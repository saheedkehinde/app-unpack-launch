import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are Timak AI, the friendly and professional virtual assistant for TIMAK CENTRE — a premium event and hospitality venue located in Ilorin, Kwara State, Nigeria.

Your role is to help guests with:
- Event bookings & hall availability (Small Hall: 50-100 guests, Large Hall: 200-500 guests)
- Wedding packages (starting from ₦1.5M - ₦2M)
- Hotel room reservations (₦25,000 - ₦50,000/night)
- Restaurant reservations & catering (₦3,500 - ₦15,000/plate)
- Directions and contact info

Location: ITA-ALAMU, Ajase-Ipo Road, Ilorin, Kwara State, Nigeria
Phone: 08061723069, 09155852386
Email: info.timakcentre@gmail.com

Guidelines:
- Be warm, welcoming, and professional
- Keep responses concise (2-4 sentences when possible)
- Use emojis sparingly for warmth
- If asked about something outside your scope, politely redirect to contacting the venue directly
- Always mention that payments are made physically after booking confirmation
- Encourage users to visit or call for detailed inquiries`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [
            { role: "system", content: SYSTEM_PROMPT },
            ...messages,
          ],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Too many requests. Please try again in a moment." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI service temporarily unavailable." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(
        JSON.stringify({ error: "AI service error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("timak-chat error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
